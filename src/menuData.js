// 모든 카테고리 목록
export const categories = Object.keys(menuData);

// 특정 카테고리에서 랜덤 메뉴 선택
export const getRandomMenuFromCategory = (category) => {
  const menus = menuData[category];
  const randomIndex = Math.floor(Math.random() * menus.length);
  return menus[randomIndex];
};

// 랜덤 카테고리 선택
export const getRandomCategory = () => {
  const randomIndex = Math.floor(Math.random() * categories.length);
  return categories[randomIndex];
};

// 완전 랜덤 메뉴 추천 (모든 카테고리에서)
export const getRandomMenu = () => {
  const category = getRandomCategory();
  const menu = getRandomMenuFromCategory(category);
  return { category, menu };
};

