// 메뉴명에서 기본 메뉴 추출 (지도 검색용)
export const getBaseMenu = (menu) => {
  if (!menu) return menu;

  // 기본 메뉴 목록에서 메뉴명에 포함된 기본 메뉴 찾기
  for (const baseMenu of baseMenus) {
    if (menu.includes(baseMenu)) {
      return baseMenu;
    }
  }

  // 기본 메뉴를 찾지 못한 경우 원본 메뉴명 반환
  return menu;
};
